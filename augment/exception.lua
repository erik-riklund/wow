--
--[ throw ]
--
-- Throws a Lua error with a formatted message.
--
throw = function(message, ...)
  error(message:format(...))
end

--
--[ catch ]
--
-- Formats an error message string using the provided arguments.
--
catch = function(message, ...)
  return message:format(...)
end
